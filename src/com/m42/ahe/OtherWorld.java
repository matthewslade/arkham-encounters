public class OtherWorld {
    int count;
    String colours;

    public OtherWorld(int count, String colours) {
        this.count = count;
        this.colours = colours;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getColours() {
        return colours;
    }

    public void setColours(String colours) {
        this.colours = colours;
    }
}
